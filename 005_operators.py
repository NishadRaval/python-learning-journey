# Arithmetic Operators
a = 10
b = 3

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Power:", a ** b)

# Comparison Operators
print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)

# Logical Operators
x = True
y = False

print(x and y)
print(x or y)
print(not x)


# Assignment Operators
num = 5

num += 2
print(num)

num -= 1
print(num)

num *= 2
print(num)

num /= 3
print(num)