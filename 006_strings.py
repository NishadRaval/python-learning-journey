# Creating Strings
name = "Nishad"
print(name)

# Indexing
print(name[0])   # N
print(name[1])   # i
print(name[2])   # s

# Negative Indexing
print(name[-1])  # d
print(name[-2])  # a

# Slicing
print(name[0:3])   # Nis
print(name[2:5])   # sha
print(name[:4])    # Nish
print(name[3:])    # had

# Length of String
print(len(name))    #6  